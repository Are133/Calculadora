using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassCalculadora.ModeloCalculadora
{
    public class ClassRecibirNumeros
    {

        public string CapturarNumeros(string numero)
        {
            if (numero == "1")
            {
                return "1";
            }

            if (numero == "2")
            {
                return  "2";
            }

            if (numero == "3")
            {
                return "3";
            }

            if (numero == "4")
            {
                return "4";
            }

            if (numero == "5")
            {
                return "5";
            }

            if (numero == "6")
            {
                return "6";
            }

            if (numero == "7")
            {
                return "7";
            }

            if (numero == "8")
            {
                return "8";
            }

            if (numero == "9")
            {
                return "9";
            }

            if (numero == "0")
            {
                return "0";
            }

            if (numero == ".")
            {
                return ".";
            }


            else
            {
                return "";
            }
        }


    }
}
